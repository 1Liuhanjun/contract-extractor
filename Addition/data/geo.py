def province_of_city(city):
    """由城市名（通常是 LEFT(线路名,2)）反查所属省；查不到返回 None。"""
    if not city:
        return None
    s = str(city).strip()
    if s in _CITY_TO_PROV:
        return _CITY_TO_PROV[s]
    # 线路名前缀可能是 3 字市名的前 2 字，做前缀兜底
    for name, prov in _CITY_TO_PROV.items():
        if name.startswith(s) or s.startswith(name):
            return prov
    return None
